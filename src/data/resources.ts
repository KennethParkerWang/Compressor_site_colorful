export type ResourceCategory =
  | 'dataset'    // 数据集
  | 'tool'       // 工具/库
  | 'framework'  // 框架
  | 'pretrained' // 预训练模型
  | 'tutorial'   // 综述/教程
  | 'project'    // 项目/案例
  | 'standard'   // 标准/规范
  | 'template'   // 模板
  | 'leaderboard'; // 排行榜源

export interface Resource {
  name: string;
  category: ResourceCategory;
  emoji: string;
  briefZh: string;
  url: string;
  coverImage?: string;
  coverAlt?: string;
  tags?: string[];
  relatedLits?: string[]; // literatureData.ts ids
}

export function getResourceCoverSlug(name: string): string {
  const ascii = name
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-{2,}/g, '-')
    .toLowerCase();
  return ascii || 'resource';
}

export function getResourceCoverImage(resource: Pick<Resource, 'name' | 'coverImage'>): string {
  return resource.coverImage ?? `/img/resource-covers/${getResourceCoverSlug(resource.name)}.png`;
}

